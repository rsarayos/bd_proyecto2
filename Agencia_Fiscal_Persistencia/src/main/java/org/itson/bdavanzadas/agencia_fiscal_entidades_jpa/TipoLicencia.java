package org.itson.bdavanzadas.agencia_fiscal_entidades_jpa;

/**
 * Enumeración que representa los tipos de duración de una licencia.
 * Los tipos de licencia pueden ser de un año, dos años o tres años.
 */
public enum TipoLicencia {
    
    // Licencia con duración de un año.
    UN_ANIO,
    // Licencia con duración de dos años.
    DOS_ANIOS,
    // Licencia con duración de tres años
    TRES_ANIOS;
    
}
